package org.hpccsystems.dashboard.entity;

public class DashboardMenu {
	
	String dashApplicationId;
	
	String dashMenuId;
	
	String dashMenuName;
	
	String dashMenuImageLocation;
	
	String dashMenuPageLocation;

	public String getDashApplicationId() {
		return dashApplicationId;
	}

	public void setDashApplicationId(String dashApplicationId) {
		this.dashApplicationId = dashApplicationId;
	}

	public String getDashMenuId() {
		return dashMenuId;
	}

	public void setDashMenuId(String dashMenuId) {
		this.dashMenuId = dashMenuId;
	}

	public String getDashMenuName() {
		return dashMenuName;
	}

	public void setDashMenuName(String dashMenuName) {
		this.dashMenuName = dashMenuName;
	}

	public String getDashMenuImageLocation() {
		return dashMenuImageLocation;
	}

	public void setDashMenuImageLocation(String dashMenuImageLocation) {
		this.dashMenuImageLocation = dashMenuImageLocation;
	}

	public String getDashMenuPageLocation() {
		return dashMenuPageLocation;
	}

	public void setDashMenuPageLocation(String dashMenuPageLocation) {
		this.dashMenuPageLocation = dashMenuPageLocation;
	}
	
	

	
	
}
